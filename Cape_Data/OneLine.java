public class OneLine
{
    private double hours;
    private String avgGrade;

    public OneLine(double hours, String avgGrade)
    {
        this.hours = hours;
        this.avgGrade = avgGrade;
    }

    public double getHours()
    {
        return this.hours;
    }

    public String getAvgGrade()
    {
        return this.avgGrade;
    }
}
